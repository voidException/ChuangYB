package com.startup.vo;

public class BaseVo {
	private int Error;
	private String ErrorMessage;
	
	public int getError() {
		return Error;
	}
	public void setError(int error) {
		Error = error;
	}
	public String getErrorMessage() {
		return ErrorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		ErrorMessage = errorMessage;
	}
	
}
